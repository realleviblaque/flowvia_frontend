export function checkAccountBadge(job) {
  if (job.clientAccountBadge === 'Self') {
    return 'self-account-type'
  } else if (job.clientAccountBadge === 'Team') {
    return 'team-account-type'
  } else if (job.clientAccountBadge === 'Recruiter') {
    return 'client-account-type'
  }
}
export function savePostToStorage() {
  localStorage.setItem('post', JSON.stringify(homePost));
}

export function reuseCount(like) {
  const {postId} = like.dataset;
  const post = homePost.find(p => p.id == postId);
  const likeImg = like.querySelector('.like-img');
  const likeCount = like.querySelector('.count');
  const likeText = like.querySelector('.like-text');

  if (!post.liked) {
    post.likes ++;
    post.liked = true;
    likeImg.src = '/Icons/bootstrap icon/hand-thumbs-up-fill.svg';
    likeText.textContent = 'Liked';
  } else {
    post.likes --;
    post.liked = false;
    likeImg.src = '/Icons/bootstrap icon/hand-thumbs-up.svg';
    likeText.textContent = 'Like';
  }
  likeCount.textContent = post.likes;
  savePostToStorage();
}

export function checkAccountType(post) {
  if (post.accountType === 'Recruiter') {
    return 'client-account-type';
  } else if (post.accountType === 'Team') {
    return 'team-account-type';
  } else if (post.accountType === 'Self') {
    return 'self-account-type'
  }
}